    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        try {
            // TODO add your handling code here:
            Imc imc = new Imc();
            imc.setCodigo(Integer.parseInt(campoCodigo.getText()));
            imc.setNome(campoNome.getText());
            imc.setPeso(Float.parseFloat(campoPeso.getText()));
            imc.setAltura(Float.parseFloat(campoAltura.getText()));
            
            //Controlar duplicidade
            if(lista.contains(imc)){
                JOptionPane.showMessageDialog(rootPane, "CÃ³digo jÃ¡ existe.");
                return;
            }
            
            lista.add(imc);
            atualizarTabela();
            limparCampos();
            
        } catch (Exception ex) {
            Logger.getLogger(TelaImc.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        // 1 - pegar a referencia do objeto que clicou
        linha = tabela.getSelectedRow();
        Imc selecionado = lista.get(linha);
        // 2 - colocar os dados do objeto no formulario
        campoCodigo.setText(Integer.toString(selecionado.getCodigo()));
        campoNome.setText(selecionado.getNome());
        campoAltura.setText(Float.toString(selecionado.getAltura()));
        campoPeso.setText(Float.toString(selecionado.getPeso()));
        
    }//GEN-LAST:event_tabelaMouseClicked

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        lista.remove(linha);
        atualizarTabela();
        limparCampos();
    }//GEN-LAST:event_botaoExcluirActionPerformed

