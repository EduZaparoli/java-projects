    private void atualizarTabela() {
        Object[][] matriz = new Object[lista.size()][5];
        linha = 0;
        lista.forEach(obj -> {
            matriz[linha][0] = obj.getCodigo();
            matriz[linha][1] = obj.getNome();
            matriz[linha][2] = obj.getPeso();
            matriz[linha][3] = obj.getAltura();
            matriz[linha][4] = obj.getClassificacao();
            linha++;
            
            
        });
        
        tabela.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "CÃ³digo", "Nome", "Peso", "Altura", "ClassificaÃ§Ã£o"
            }
        ));
    }

    private void limparCampos() {
        campoCodigo.setText("");
        campoNome.setText("");
        campoPeso.setText("");
        campoAltura.setText("");
    }
