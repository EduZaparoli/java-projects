    int linha;
    private void atualizarTabela() {
       Object[][] matriz = new Object[lista.size()][8];
       linha = 0;  
       lista.forEach(obj ->{
          matriz[linha][0] = obj.getMatricula();
          matriz[linha][1] = obj.getNotaUm();
          matriz[linha][2] = obj.getNotaDois();
          matriz[linha][3] = obj.getNotaTres();
          matriz[linha][4] = obj.getNotaRecuperacao();
          matriz[linha][5] = obj.getNotaExame();
          try {
              matriz[linha][6] = obj.getMediaFinal();
          } catch (Exception ex) {
              matriz[linha][6] = ex.getMessage(); 
          }
          try {
              matriz[linha][7] = obj.getSituacao();
          } catch (Exception ex) {
              matriz[linha][7] = ex.getMessage();
          }
          linha++;
       });
        
       tabela.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Matrícula", "N1", "N2", "N3", "Rec", "Exa", "Med", "Sit" 
            }
        ));                
    }

