    private void botaoIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoIncluirActionPerformed
        try {
            // TODO add your handling code here:
            Avaliacao a = new Avaliacao();
            a.setMatricula(Integer.parseInt(campoMatricula.getText()));
            a.setNome(campoNome.getText());
            if (campoNotaUm.getText().isEmpty())
                a.setNotaUm(null);
            else
                a.setNotaUm(Float.parseFloat(campoNotaUm.getText()));
            
            if (campoNotaDois.getText().isEmpty())
                a.setNotaDois(null);
            else
                a.setNotaDois(Float.parseFloat(campoNotaDois.getText()));            
            
            if (campoNotaTres.getText().isEmpty())
                a.setNotaTres(null);
            else
                a.setNotaTres(Float.parseFloat(campoNotaTres.getText()));

            if (campoRec.getText().isEmpty())
                a.setNotaRecuperacao(null);
            else
                a.setNotaRecuperacao(Float.parseFloat(campoRec.getText()));            
            
            if (campoExame.getText().isEmpty())
                a.setNotaExame(null);
            else
                a.setNotaExame(Float.parseFloat(campoExame.getText()));            
            
            lista.add(a);
            atualizarTabela();
            
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_botaoIncluirActionPerformed

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        // TODO add your handling code here:
        // 1) Pegar a referência do objeto que clicou
        linha = tabela.getSelectedRow();
        Avaliacao selecionado = lista.get(linha);
        // 2) Colocar os dados do objeto no formulário
        campoMatricula.setText(selecionado.getMatricula().toString());
        campoNome.setText(selecionado.getNome());
        campoNotaUm.setText(selecionado.getNotaUm() != null ?
                selecionado.getNotaUm().toString():"");
        campoNotaDois.setText(selecionado.getNotaDois() != null ?
                selecionado.getNotaDois().toString():"");
        campoNotaTres.setText(selecionado.getNotaTres() != null ?
                selecionado.getNotaTres().toString():"");
        campoRec.setText(selecionado.getNotaRecuperacao() != null ?
                selecionado.getNotaRecuperacao().toString():"");
        campoExame.setText(selecionado.getNotaExame() != null ?
                selecionado.getNotaExame().toString():"");
        
    }//GEN-LAST:event_tabelaMouseClicked

