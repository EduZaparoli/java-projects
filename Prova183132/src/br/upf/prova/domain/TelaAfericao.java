    private void atualizarTabela() {
       Object[][] matriz = new Object[lista.size()][4];
       linha = 0;  
       lista.forEach(obj ->{
          matriz[linha][0] = obj.getCpf();
          matriz[linha][1] = obj.getNome();
          matriz[linha][2] = obj.getTemperatura();
          matriz[linha][3] = obj.getClassificacao();
          linha++;
       });
        
       tabela.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Cpf", "Nome", "Temperatura", "Classifica√ß√£o" 
            }
        ));                
    }


