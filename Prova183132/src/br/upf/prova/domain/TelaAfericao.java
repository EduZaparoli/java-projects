    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        try {
            
            Prova183132 a = new Prova183132();
            a.setCpf(campoCpf.getText());
            a.setNome(campoNome.getText());
            a.setTemperatura(Float.parseFloat(campoTemperatura.getText()));
            
            lista.add(a);
            atualizarTabela();
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        try {
            Prova183132 a = new Prova183132();
            a.setCpf(campoCpf.getText());
            a.setNome(campoNome.getText());
            a.setTemperatura(Float.parseFloat(campoTemperatura.getText()));
            if (campoCpf.getText().isEmpty())
                a.setCpf(null);
            else
                a.setCpf(campoCpf.getText());
            
            if (campoNome.getText().isEmpty())
                a.setNome(null);
            else
                a.setNome(campoNome.getText());
            
            if (campoTemperatura.getText().isEmpty())
                a.setTemperatura(null);
            else
                a.setTemperatura(Float.parseFloat(campoTemperatura.getText()));
                   
            // CONTROLAR SE EXISTE PARA SER ALTERADO
            if (!lista.contains(a)){
              JOptionPane.showMessageDialog(rootPane, "Objeto n√£o existe.");
              return;
            }
            
            lista.set(linha, a);
            atualizarTabela();   
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        } 
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        lista.remove(linha);
        atualizarTabela();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        linha = tabela.getSelectedRow();
        Prova183132 selecionado = lista.get(linha);
        
        campoCpf.setText(selecionado.getCpf());
        campoNome.setText(selecionado.getNome());
        campoTemperatura.setText(selecionado.getTemperatura().toString());
    }//GEN-LAST:event_tabelaMouseClicked

