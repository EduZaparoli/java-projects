    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
       try {
            Avaliacao avaliacao = new Avaliacao();
            avaliacao.setNome(campoNome.getText());
            avaliacao.setMatricula(Integer.parseInt(campoMatricula.getText()));
            avaliacao.setNotaUm(Float.parseFloat(campoNota1.getText()));
            avaliacao.setNotaDois(Float.parseFloat(campoNota2.getText()));
            avaliacao.setNotaTres(Float.parseFloat(campoNota3.getText()));
            avaliacao.setNotaRecuperacao(Float.parseFloat(campoRecuperacao.getText()));
            avaliacao.setNotaExame(Float.parseFloat(campoExame.getText()));

            lista.add(linha, avaliacao);
            atualizarTabela();
            limparCampos();
        } catch (Exception exception) {
        }
    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
           try {

            Avaliacao avaliacao = new Avaliacao();
            if(!campoNome.getText().isEmpty())
               avaliacao.setNome((campoNome.getText()));
            if(!campoMatricula.getText().isEmpty())
               avaliacao.setMatricula(Integer.parseInt(campoMatricula.getText()));
            if(!campoNota1.getText().isEmpty())
                avaliacao.setNotaUm(Float.parseFloat(campoNota1.getText()));
            if(!campoNota2.getText().isEmpty())
                avaliacao.setNotaDois(Float.parseFloat(campoNota2.getText()));
            if(!campoNota3.getText().isEmpty())
                avaliacao.setNotaTres(Float.parseFloat(campoNota3.getText()));
            if(!campoRecuperacao.getText().isEmpty())
                avaliacao.setNotaRecuperacao(Float.parseFloat(campoRecuperacao.getText()));
            if(!campoExame.getText().isEmpty())
                avaliacao.setNotaExame(Float.parseFloat(campoExame.getText()));
           
            lista.set(indexLinha, avaliacao);
            atualizarTabela();
            limparCampos();
           
           
        } catch (Exception ex) {
            // mostrar erro
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

