    private void atualizarTabela() {
        Object[][] matriz = new Object[lista.size()][9];
        linha = 0;
        lista.forEach(obj -> {
            matriz[linha][0] = obj.getNome();
            matriz[linha][1] = obj.getMatricula();
            matriz[linha][2] = obj.getNotaUm();
            matriz[linha][3] = obj.getNotaDois();
            matriz[linha][4] = obj.getNotaTres();
            matriz[linha][5] = obj.getNotaRecuperacao();
            matriz[linha][6] = obj.getNotaExame();
            try {
                matriz[linha][7] = obj.getMedia();
                matriz[linha][8] = obj.getMediaFinal();
            } catch (Exception exception) {
            }
            linha++;
            
            
        });
        
        tabela.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Nome", "Matricula", "Nota 1", "Nota 2", "Nota 3", "Recuperação", "Exame"
            }
        ));
    }

